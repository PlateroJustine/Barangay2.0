    private void loadCensusData() {
        try (Connection conn = DriverManager.getConnection(url, username, password)) {
            // SQL query to retrieve data from the census table
            String query = "SELECT * FROM census";
            PreparedStatement pst = conn.prepareStatement(query);
            ResultSet rs = pst.executeQuery();

            // Create a DefaultTableModel to hold the data for the JTable
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);  // Clear existing rows before adding new ones

            // Loop through the result set and add each row to the table
            while (rs.next()) {
                String year = rs.getString("year");
                String totalPopulation = rs.getString("total_population");
                String malePopulation = rs.getString("male_population");
                String femalePopulation = rs.getString("female_population");
                String dateRecorded = rs.getString("date_recorded");

                // Add the row to the table model
                model.addRow(new Object[]{year, totalPopulation, malePopulation, femalePopulation, dateRecorded});
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }
