    private Connection connectDB() {
    Connection conn = null;
    try {
        // Load MySQL JDBC Driver
        Class.forName("com.mysql.cj.jdbc.Driver");
        
        // Define database URL, username, and password
        String url = "jdbc:mysql://localhost:3306/barangay_db2?serverTimezone=UTC";
        String user = "root";
        String password = ""; // Change this if you have a password
        
        // Establish Connection
        conn = DriverManager.getConnection(url, user, password);
        System.out.println("Database Connection Successful!");
        
    } catch (ClassNotFoundException e) {
        JOptionPane.showMessageDialog(this, "JDBC Driver not found! " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    } catch (java.sql.SQLException e) {
        JOptionPane.showMessageDialog(this, "Database Connection Failed! Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    return conn;
}

