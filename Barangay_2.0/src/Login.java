    private void LoginButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginButtonActionPerformed
        String username = jTextField1.getText();
        String password = new String(jPasswordField1.getPassword()); // Get password from input

        if (username.isEmpty() || password.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter username and password", "Warning", JOptionPane.WARNING_MESSAGE);
            return;
        }

        try (Connection conn = connectDB()) {
            if (conn != null) {
                // Ensure hashing matches the stored hash
                String sql = "SELECT * FROM admin WHERE username = ? AND password_hash = SHA1(?)";
                PreparedStatement pstmt = conn.prepareStatement(sql);
                pstmt.setString(1, username);
                pstmt.setString(2, password); // Hashing applied in SQL query

                ResultSet rs = pstmt.executeQuery();

                if (rs.next()) {
                    // Directly open the Main Dashboard without showing a JOptionPane
                    MainDashboard mainDashboard = new MainDashboard();
                    mainDashboard.setVisible(true);

                    this.dispose(); // Close the login form
                } else {
                    JOptionPane.showMessageDialog(this, "Invalid Username or Password", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_LoginButtonActionPerformed

